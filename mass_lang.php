    function eLabel($key) {
        if (array_key_exists($key,$GLOBALS['labels'])) {
          return $GLOBALS['labels'][$key][$GLOBALS['lang']['label']];
        } else {
          return "???";
        }
    }

    function eText($key) {
        if (array_key_exists($key,$GLOBALS['texts'])) {
            return $GLOBALS['texts'][$key][$GLOBALS['lang']['text']];
        } else {
            return "???";
        }
    }

    function genLink($fL = '', $fT = '') {
        $getL = $fL != '' ? $fL : $GLOBALS['lang']['label'];
        $getT = $fT != '' ? $fT : $GLOBALS['lang']['text'];
        return "index.php?ll=${getL}&lt=${getT}";
    }

